<p><h1 style="float:none;">Center for Jewish Life</h1></p>

<p>The Center for Jewish Life/Hillel at Princeton University is the central source for all things Jewish at Princeton. The Center for Jewish Life (or CJL) believes in creating a community where all students, faculty, staff and even the larger community can come together to take part in the rich tapestry of Jewish Life at Princeton. The CJL is committed to creating a community that is welcoming of all people; Jews and non-Jews, religious and secular, men and women. More information is available at the <a href="http://www.princetonhillel.org">CJL's website</a>.</p>



