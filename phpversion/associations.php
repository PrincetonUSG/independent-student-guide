<p><h1 style="float:none;">2 Dickinson Street Co-op</h1></p>

<p>2 Dickinson Street (2D) is an organized independent dining option open to all. Members pay just $500 per semester, and cook dinner once a week with other members (no cooking experience necessary). Members can come any time of the day or night to use the co-op kitchen and enjoy leftovers, fruit, cheese, yogurt, milk, and juice from the co-op fridges and freezers or cereal, fresh bread, and sandwich fillings such as hummus, jam, and almond butter from the well-stocked pantry. Fresh-cooked dinner is served every evening. Recent meals have included mushroom risotto, fondue, vegetable sushi, avacado salad, cookies, curries, Dal, stir-fries, and much more. The kitchen is 100% vegetarian, though not everyone in 2D is vegetarian, and vegan options are available at every meal. Guests interested in the co-op are welcome to visit for dinner any day at 6:30pm. Make sure to check out <a href="http://www.princeton.edu/~twod">2D's website</a>.</p>



