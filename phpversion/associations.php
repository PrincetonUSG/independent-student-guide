<p><h1 style="float:none;">Brown Co-op</h1></p>

<p>The Brown Co-op is a 26 member dining cooperative for upperclass students at Princeton University. Students pool their resources to buy food, cook once a week in small teams and have evening meals together. For more information, <a href="http://www.princeton.edu/~brwncoop/">check out the website of the Brown Co-op</a>.</p>



