<p><strong>Limited-access Kitchens:</strong></p>
<ul>
<li>Limited-access kitchens, located in the 1901/Laughlin Halls, Lockhart, and the Pyne basements, are locked and are accessible to a limited number of students who sign up. Note: Only Brown Co-op members may sign out a Brown second floor key.</li>
<li>Independent students who wish to participate may so indicate at the time of draw, and their kitchen keys will be made available at check-in. Pyne kitchen is limited to 25 members and 1901/ Laughlin to 20. Spaces remaining open in the kitchens after room draw are available to any student on a first-come, first-served basis at the beginning of fall semester classes.</li>
<li>The limited access kitchens are locked and have a limited number of "members." There will be no charge for the use of these kitchens. The following items are available in each limited-access facility: tables and chairs, a full size refrigerator, a range, a toaster and a microwave oven. Students may store and use kitchen appliances brought from home, provided appliances are kept clean and in good working condition. Be cautioned that all heat-producing appliances are strictly prohibited in student rooms. Limited access kitchens are randomly inspected to ensure the maintenance of fire safety codes.</li>
<li>Independent students who wish to have use of a cabinet or access to a limited access kitchen may sign up at the time of the Independent draw by checking the kitchen key box after selecting a room. Print out the form, fill it out, and fax it to the housing office. Your keys will be available at check-in. There is no charge for keys. Cabinet spaces remaining open in these kitchens after the Independent draw are available to any student on a first-come, first-served basis through May 1. After this date, students may not sign up for cabinet keys until after the start of classes in September. To sign up for a kitchen cabinet key after the start of classes, go to the Housing Department in the MacMillan Building.</li>
<li>Key holders are cooperatively responsible for cleaning their respective kitchens. Kitchen members should therefore be prepared to take part in a weekly cleaning schedule in addition to routine clean up after individual use of the kitchen area and utensils. Building Services will provide brooms, mops, garbage bags and some cleaning products, but does not assist in the actual upkeep of limited access kitchens.</li>
</ul>

