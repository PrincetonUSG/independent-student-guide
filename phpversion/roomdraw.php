<div style="width:60%;"><p>The University defines an Independent student as one who holds no meal contract whatsoever with a University dining facility or with an off-campus club. In most instances, the Independent student prepares his or her own meals on a regular basis, which makes appropriate cooking facilities necessary. To accommodate this need, the Spelman Hall Buildings 1-6, 2 Dickinson Street Co-op, and Independent Draws are offered.</p></div>
 
<p>The application for these draws will have an Independent box to check if the student wishes to be an independent. When a room is chosen and the Housing/Dining Agreement is electronically signed, a pledged Independent is acknowledging adherence to the following statement:<br /><div class="blockquote">"My signature on the Housing/Dining Agreement acknowledges my full understanding of the University's definition of an Independent. I certify by my signature that I am aware if I fail to abide by the Pledge, I will be referred to the Office of the Dean of Undergraduate Students for disciplinary action. If found in violation of the Independent Pledge, I understand that the penalty is normally probation. I am aware that probation is a matter of permanent record."</div></p>
 
<p>By electronically signing the Housing/Dining Agreement at the time of room selection, the student certifies his or her status as an Independent for the entire academic year. You may not select a room as an Independent and then decide later to join a club, or take a contract meal plan. If you decide you no longer wish to be Independent after you have drawn an independent space, you must speak with the Housing Department to determine your options.</p>
 
<p>There is no wait list or room improvement for these draws. Rooms designated for these draws but NOT selected will be made available in subsequent draws. If a student enters but then decides not to draw in any of the independent draws, there is no penalty. NOTE: If a student applies but does not select a room in any of these draws and wishes to participate in the Upperclass Draw, he/she must have submitted the Upperclass Draw Application before the deadline. Please note that all applications are due by March 5, 2008 with draws starting March 25.</p>
 
<p>Each group is given a weighted rating which is the average of the points of each draw group member. The points rating for the independent draw will be as follows (please note that independent rating points for these draws do not carry over to Regular Upperclass draw):<br /><div class="blockquote">Class of 2009 Independent: 5 points<br />
Class of 2010 Independent: 4 points<br />
Class of 2009: 3 points<br />
Class of 2010: 2 points</div></p>





<div class="bigbreak"></div>



<p><h1>Independent<br />Draw</h1>A group of Upperclass dormitory rooms in close proximity to kitchens are designated as independent rooms and comprise the housing pool for this draw (see listing below). Only these rooms and any rooms remaining from the 2 Dickinson Co-op draw or Spelman draw (see above) may be chosen during the Independent Draw.</p>
 
<p>Any student or group of students who wish to participate in the Independent Draw must complete an application by March 5, 2008. Groups of eight or fewer may enter the Independent Draw, but at least 50% of the members of any group must be Independent.  For example, if a group is made up of two Independents and two non-Independents, they may split at the time of draw and draw two doubles as long as one-half the members of each sub-group are Independents. They cannot split and draw four singles.  If it's a mixed gendered group, at least 50% of each gender must be independent. In the example above, if the two independents were male and the two non independents were female, the females would not be able to draw a room because of the 50% rule.</p>
 
<p>Check Housing's website for <a href="http://www.princeton.edu/facilities/housing/undergraduate_info/draw_guide_2008/#comp000047a089800000000aa27895">rooms that are available for Independent Draw.</a></p>

<p>Independent students who participate in Independent Draw may sign up for kitchen keys at the time of the Independent draw. These kitchen keys will be available at check-in.  A listing of kitchens in the Upperclass dorms is located on the <a href="cooking.php">Kitchens & Cooking page</a> of this guide.</p>





<div class="bigbreak"></div>





<p><h1>Spelman<br />Draw</h1>Groups of four or eight students from the classes of 2010 and 2009 may enter the Spelman Draw by submitting an application. All quads except those in Buildings 7 and 8 will be available.</p>

<p>Each group of four must be the same gender and have at least two independents. Groups with fewer than two independents are not eligible for this draw and will not receive a draw time. If you are forming a group for Spelman, consider carefully the added cooperation necessary to cook and clean together. Kitchen space is in high demand in Princeton dorms and is therefore set aside for Independent usage. Spelman housing is designated for the availability of its kitchen facilities to those with no eating affiliations. If you plan to eat as a member of a co-op (Brown or 2 Dickinson) or an eating club, you may not draw into Spelman as an Independent.</p>
 
<p>Helpful hint: When the group is choosing more than one room, make sure there are the correct amount of independents in each room before hitting the final submit button.</p>




<div class="bigbreak"></div>




<p><h1>2 Dickinson<br />Street Draw</h1>The house at 2 Dickinson Street will continue for the 2008-2009 academic year as a cooperative. This is a self-governing co-op, and has historically served vegetarian meals. Any student or group of students (eight or fewer) who wish to participate in the 2 Dickinson Co-op must submit an application by March 5, 2008. If you select a room in this draw you must be an independent (the independent pledge box will automatically be checked for this draw).</p>
 
<p>Any rooms not drawn in the 2 Dickinson Draw will be placed in subsequent draws. Independent students who draw 2 Dickinson rooms in the Independent Draw are not required to join the cooperative, and shall be granted kitchen privileges. </p>




