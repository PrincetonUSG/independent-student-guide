<p><h1 style="float:none;">Why go<br />independent?</h1></p>

<p><strong>The opportunity to be self-sufficient</strong>. "I like the idea of being (a little more) self-sufficient and cooking my own meals." (Sam Borchard)</p>

<p><strong>Cost</strong>. "It's cheaper." (Sam Borchard) "I spent about $2500 on food for the year. However, it is possible to spend less than this because I ate out at restaurants a couple times a week. I'd say I spent about $80 per week on food for a total of 30 weeks." (Zaafir Kherani) "Besides possibly eating better, in all likelihood I would save thousands of dollars by going independent, and the difference between being a member or friend of members at an eating club didn't seem to be worth that much." (Ryan Irwin)</p>

<p><strong>More options for vegetarians and vegans</strong>. "As a vegetarian, I will probably eat better as an independent than I would in a club." (Sam Borchard)</p>

<p><strong>Health/Quality of Food</strong>. "It's easier to eat more local/organic food." (Sam Borchard)</p>

<p><strong>Housing</strong>. "The rooms are pretty sweet." (Sam Borchard)</p>

<p><strong>Lack of Interest in Clubs</strong>. "I chose to go independent because I do not feel like I would get anything out of being in an eating club, since I don't drink." (Josephine Walker)</p>

<p><strong>Love of Cooking</strong>. "I also enjoy cooking and I'm excited to have my own kitchen and cook." (Josephine Walker)</p>

<p><strong>Proximity</strong>. "[N]ot having to walk all the way to the Street to eat" is an incentive. (Jonathan Giuffrida)</p>

<p><strong>Flexibility</strong>. "Going independent gave me flexibility to eat when and where I want." (Zaafir Kherani)</p>




<div class="bigbreak"></div>




<p><h1>Why join<br />a co-op?</h1>"I joined Brown in order to have a nice <strong>laid-back social environment</strong> to supplement some additional meals." (Raleigh Martin)</p>




<div class="bigbreak"></div>




<p><h1>What do you<br />wish you had<br />known?</h1>It can be <strong>hard to find housing</strong>. "Before I went independent, I wish I had known that Spelman would fill up this year, and that it would be much more difficult than I expected to find a place to live with a few friends to create an informal dinner group.  I don't know if it's just the shortage of housing this year, but my friends and I have been badly split up and I may have to cook for myself a lot more often than I expected, which means a lot more time out of my schedule." (Jonathan Giuffrida)</p>



<div class="bigbreak"></div>



<p><h1>Advice</h1><strong>Think through your decision</strong>. "Be sure that you would still go independent even if it means getting stuck in a double or single, and having to provide all your own meals.  It's nice to choose what you eat and save money, but it's a lot of time." (Jonathan Giuffrida)</p>

<p>"<strong>Join a co-op!</strong>  It has all the great aspects of independence (cheapness, home-cooked food) but also great convenience (a warm meal every night!)." (Raleigh Martin)</p>

<p>"<strong>Get a car</strong> or bring one from home. It is much more convenient to get groceries and other supplies that you will need if you're independent when you have a car. Taking the bus or finding rides with other people will soon get frustrating." (Zaafir Kherani)</p>


