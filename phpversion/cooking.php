<p><h1>Healthy<br />Eating</h1>
One of the greatest parts about going independent is having the freedom to choose what you eat. Just remember to make it healthy! Below are some websites to help you figure out how.</p>

<p>Remember learning about the food pyramid in first grade?  <a href="http://www.mypyramid.gov/">This site</a> introduces you to the new and improved pyramid and also has a menu planner to help you develop balanced meals based on your recommended caloric intake.</p>

<p><a href="http://vm.cfsan.fda.gov/~dms/foodlab.html">Food labels</a>: Guidance on how to understand and use the nutrition facts panel on food labels.</p>

<p><a href="http://www.dietitian.com/">Dietitian.com</a>: Q&A, Healthy Body Calculator, Tips to Prevent Weight Gain, etc.</p>

<p><a href="http://homecooking.about.com/od/foodhealthinformation/a/collegefood.htm">About.com, Home Cooking</a>: USDA approved article on college health, including links to recipe & fitness sites</p>

<p><a href="http://www.princeton.edu/facilities/dining_services/nutritional_info/">The Dining Services Nutrition Page</a>: info and monthly articles, plus contact information for the campus dietitician. It's especially helpful for anyone who works out.</p>

<p><a href="http://www.veganhealth.org">VeganHealth.org</a>: Resources for vegans and vegetarians, including recommended daily vitamin intake.</p>



