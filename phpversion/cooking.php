<div style="width:50%;"><p><h1 style="float:none;">Cooking <span class="amp">&amp;</span><br />Recipe Resources</h1></p><p><a href="http://www.foodreference.com">Foodreference.com</a>: Provides extensive information about all things food-related, including recipes.</p>
<p><a href="http://www.foodsubs.com">Cook's Thesaurus</a>: This site has the scoop on any ingredient. Where else can an amateur cook find out how it's eaten and prepared, possible substitutes, and how to tell when it's no longer fresh?</p></div>

<p><a href="http://www.cookingbynumbers.com/frames.html">Cooking By Numbers</a>: Check off what you have from the site's list of 50 food staples, and presto - relevant recipes appear, rated 100 percent (if you have all the ingredients) or a lesser percentage (if you lack a few). While four-star chefs may find the pickings slim, the site is perfect for your everyday independent student.</p>

<p><a href="http://www.allrecipes.com">All Recipes</a>: Simply the most popular recipe site on the web</p>

<p><a href="http://www.yumyum.com/student/">YumYum.com</a>
While students do not have much time to shop for food or prepare meals, they still need to eat. Most of the following recipes can be prepared in an apartment, dorm, or house as you complete one of the other 101 tasks you need to accomplish.</p>

<p><a href="http://www.aboutcollege.com/recipe.htm">AboutCollege Recipes</a>: Quick and cheap quintessential college food.</p>

<p><a href="http://www.mnsu.edu/shs/healtheducation/bmc/">Beyond Mac & Cheese</a>: Easy, cheap recipes with detailed nutrition information and estimated cost.</p>

<p><a href="http://www.epicurious.com">Epicurious.com</a>: Provides more sophisticated recipes from Gourmet and Bon Apetit magazines.</p>

<p><a href="http://www.vegweb.com/food/">VegWeb</a>: Their slogan is "Vegetarians Unite!" If you want to be a vegetarian for life, or just for one meal, there are hundreds of great recipes here!</p>

<p>Have a great recipe of your own you'd like to share? Go to the Independent Student thread on <a href="http://point.princeton.edu/tigerforums/viewforum.php?f=28">Tigerforums</a>!</p>




