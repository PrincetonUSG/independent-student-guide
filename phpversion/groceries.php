<div style="width:60%;"><p><h1>Groceries<br />Delivery</h1><strong>McCaffrey's Free Delivery Service:</strong> Princeton McCaffrey's is a grocery store located in Princeton Shopping Center, a destination of both the NJ Transit bus system (routes 605 or 606) and Princeton University's jitney system. In addition to the specialty services listed on McCaffrey's website, they offer all basic groceries at competitive prices.</p>

<p>The USG has negotiated a special contract with Princeton McCaffrey's: with a minimum order of $25, Princeton McCaffrey's will deliver your groceries free of charge to your dorm room.</p>
<p>Guidelines:
<ul>
<li>When placing an order, give the following information: Name, Delivery Address, Brief directions to delivery site (i.e. general location on campus, landmarks, nearby intersections), Contact phone number, Method of payment, Order with specific sizes/quantities and names of products requested.</li>
<li>Place your order by 4:00 p.m. for a guaranteed next day delivery between 12:00 noon and 4:00 p.m. No specific time can be requested.</li>
<li>There is a $25 minimum on each order</li>
<li>Payments can be made by cash or check upon delivery or by credit card when ordered via fax (609.683.1893) or phone (609.683.1600)</li>
</ul></p>
